export function getAllCitadelPaints(): CitadelPaintData[] {
  return CITADEL_PAINTS;
}

export function getCitadelPaintsByType(type: string): CitadelPaintData[] {
  return CITADEL_PAINTS.filter(paint => paint.type.toLowerCase() === type.toLowerCase());
}

export function searchCitadelPaints(query: string): CitadelPaintData[] {
  const lowercaseQuery = query.toLowerCase();
  return CITADEL_PAINTS.filter(paint => 
    paint.name.toLowerCase().includes(lowercaseQuery) ||
    paint.type.toLowerCase().includes(lowercaseQuery)
  );
}

export function getCitadelPaintByName(name: string): CitadelPaintData | undefined {
  return CITADEL_PAINTS.find(paint => 
    paint.name.toLowerCase() === name.toLowerCase()
  );
}